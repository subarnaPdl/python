first_name = "Subarna"
last_name = "Poudel"

# Concatenating strings
full_name = first_name + ' ' + last_name
print(full_name)

# Getting a letter from string
greet = "Namaste"
print(greet[4])

# Length of String
print(len(greet))

# Slicing the string from start
# Contains letter from 0th position but not from 3rd position
print(greet[0:3])

# Slicing the string from end
print(greet[-4:])

# Slicing with skip
print(greet)
print(greet[0:5:2])
